function storageAvailable(type) {
  try {
    var storage = window[type],
      x = "__storage_test__";
    storage.setItem(x, x);
    storage.removeItem(x);
    return true;
  } catch (e) {
    return (
      e instanceof DOMException &&
      // everything except Firefox
      (e.code === 22 ||
        // Firefox
        e.code === 1014 ||
        // test name field too, because code might not be present
        // everything except Firefox
        e.name === "QuotaExceededError" ||
        // Firefox
        e.name === "NS_ERROR_DOM_QUOTA_REACHED") &&
      // acknowledge QuotaExceededError only if there's something already stored
      storage.length !== 0
    );
  }
}


const timeManager = {
  time: 0,
  ellapsed: 0,
  timerTimeout: 0,
  timerInterval: 0,
  refreshInterval: 0,
  onTimeout: null, // function to execute on timeout

  changeTimeShown(showEllapsed, newTime) {
    let timeShown = this.ellapsed
    if (!showEllapsed) { // if ellapsed time is shown no update to the time variable
      this.time = newTime
      timeShown = this.time
    }
    document.querySelector("#time").textContent = Math.floor(timeShown/60) + ":"
      + (timeShown%60<10 ? "0"+timeShown%60 : timeShown%60)
  },
  refreshOverTime() {
    if (this.time>0) {
      const refreshInterval = setInterval(() => {
        this.changeTimeShown(false, this.time-1)
        if (this.time <= 0) {
          clearInterval(refreshInterval)
        }
      }, 1000)
      this.refreshInterval = refreshInterval
    }
  },
  setTimeOut(func) {
    this.onTimeout = func
    this.startTimer(false)
    this.refreshOverTime()
    const timerTimeout = setTimeout(() => {
      this.stopTimer()
      func()
      clearTimeout(timerTimeout)
    }, this.time*1000)
    this.timerTimeout = timerTimeout
  },
  cancelTimeout() {
    clearTimeout(this.timerTimeout)
    this.stopTimer()
    this.timerTimeout = 0
  },

  // For pausing/resuming the game
  pauseTimeout() {
    clearTimeout(this.timerTimeout)
    clearInterval(this.refreshInterval)
    this.ellapsed = this.stopTimer()
    this.timerTimeout = 0
  },
  resumeTimeout() {
    this.setTimeOut(timeManager.onTimeout)
  },

  // for couting time ellapsed since the start
  startTimer(doShow) {
    const timerInterval = setInterval(() => {
      ++this.ellapsed

      console.log(this.ellapsed)

      if (doShow) {
        this.changeTimeShown(true, -1)
      }
    }, 1000)
    this.timerInterval = timerInterval
  },
  stopTimer() {
    const ellapsedTime = this.ellapsed
    clearInterval(this.timerInterval)
    this.ellapsed = 0
    return ellapsedTime
  }
}


const game = {
  gameState: "off",
  difficulty: "",
  mode: "",
  modeTime: -1,
  passageEnd: false,
  pb: 0,
  typed: 0,
  mistakes: 0,
  mistakesPermanent: 0,
  totalPlayed: 0,

  /**
   * Replaces the current text of the #text-model element with
   * a new random one, putting each character inside a span
   * 
   * @returns A randomly model text selected for the test
   */
  generateTextModel() {
    const textModel = document.querySelector("#text-model")
    const textTyped = document.querySelector("#text-typed")

    // Deleting previous text
    while (textModel.lastChild) {
      textModel.removeChild(textModel.lastChild)
      textTyped.value = ""
    }

    const putCharsInSpans = (text) => {
      for (i=0; i<text.length; i++) {
        const newSpan = document.createElement("span")
        newSpan.textContent = text.charAt(i)
        textModel.appendChild(newSpan)
      }
      
      const endOfText = document.createElement("span")
      endOfText.classList.add("end-of-text")
      textModel.appendChild(endOfText)
    }

    switch (this.difficulty) {
      case "easy":
          putCharsInSpans(data.easy[Math.floor(Math.random()*data.easy.length)].text)
          break;
      case "medium":
          putCharsInSpans(data.medium[Math.floor(Math.random()*data.medium.length)].text)
          break;
      case "hard":
          putCharsInSpans(data.hard[Math.floor(Math.random()*data.hard.length)].text)
          break;
      default:
          console.error("Error: invalid difficulty")
          return;
    }

    textModel.firstChild.id = "next-char" // Highlighting first character
  },

  checkModel(wasCharacterTyped) {
    const textModel = document.querySelector("#text-model")
    const textTyped = document.querySelector("#text-typed").value
    const nextCharIdx = textTyped.length
    
    console.log("Debug: text typed: ", textTyped)

    if (nextCharIdx <= textModel.textContent.length) {
    // Remove the styling on the previous character and apply it onto the new one
      const previousChar = document.querySelector("#next-char")
      const nextChar = textModel.children[nextCharIdx]

      const typedChar = textTyped.charAt(nextCharIdx-1)

      // Resetting the next-char style
      previousChar.id = ""
      previousChar.classList.remove("correct", "incorrect")

      // Check if we just wrote or deleted
      if (previousChar.nextElementSibling === nextChar) { // if we just wrote...
        if (wasCharacterTyped) {
          this.typed++
        }
        // Check if the character corresponds
        if (typedChar === previousChar.textContent) {
          previousChar.classList.add("correct")
        } else {
          this.mistakes++
          this.mistakesPermanent++
          previousChar.classList.add("incorrect")
        }
      } else {
        if (nextChar.classList.contains("incorrect")) {
          this.mistakes--
        }
      }
      // Apply style to the next character to type 
      nextChar.classList.remove("correct", "incorrect")
      nextChar.id = "next-char"

      // Check if passage has ended
      this.passageEnd = nextChar.classList.contains("end-of-text")
    } else {
      this.passageEnd = true
    }
  },

  computeWpm() {
    return timeManager.ellapsed == 0 ? 0
    : Math.max(0,
        Math.round( ((Math.floor((this.typed - this.mistakes)/5))) / timeManager.ellapsed * 60))
  },
  computeFinalWpm() {
    console.assert(this.modeTime > 0);
    return Math.max(0, 
      Math.round( ((Math.floor((this.typed - this.mistakes)/5))) / this.modeTime * 60))
  },
  computeAccuracy() {
    return this.typed==0 ?
      "100%" : Math.round((1 - (this.mistakesPermanent/this.typed))*100) + "%"
  },

  onInputTextTyped: function(event) {
    game.checkModel(event.data!=null)
    document.querySelector("#wpm-score").textContent = game.computeWpm()
    document.querySelector("#acc-score").textContent = game.computeAccuracy()

    if (game.passageEnd) {
      switch (game.mode) {
        case "timed":
          game.passageEnd=false
          game.generateTextModel()
          break
        case "passage":
          game.modeTime = timeManager.stopTimer()
          game.endGame()
          break
        default:
          console.error("Error: onInputTextTyped: invalid game.mode value")
      }
    }
  },

  startGame() {
    console.log("Debug: game start")
    this.gameState = "start"

    // Disabling settings buttons
    document.querySelectorAll(".settings").forEach((buttons) => {
      buttons.classList.add("settings-disabled")
    })

    document.querySelector("#restart-bar").style.display = ""

    const textTyped = document.querySelector("#text-typed")
    // Enabling text area
    document.querySelector("#text-typed").disabled = false

    // Game loop
    if (timeManager.time>0) { // Timed mode
      // One way to remove an event listener: 
      timeManager.setTimeOut(() => {
        this.endGame()
      })
    } else { // Passage mode
      timeManager.startTimer(true)
    }
    textTyped.addEventListener("input", game.onInputTextTyped)
  },

  resetGame: function () {
    game.gameState = "off"

    /**
     * Initializing on game load
     */

    // Resetting game stats and variables (also used for initialisation)
    game.difficulty = document.querySelector("div.settings input[name='difficulty']:checked").value
    game.mode = document.querySelector("div.settings input[name='mode']:checked").value>0 ? "timed" : "passage"
    game.passageEnd = false
    game.typed = 0
    game.mistakes = 0
    game.mistakesPermanent = 0
    // Pb and totalTyped set on window load

    game.generateTextModel()

  /**
   * Resetting after game finished / after restart
   */

    // Removing every event added in startGame
    document.querySelector("#text-typed").removeEventListener("input", game.onInputTextTyped)

    // Disable "resume" overlay
    document.querySelector("#resume-msg").style.display = "none"
    // Disable text area
    document.querySelector("#text-typed").disabled = true

    // Enable settings buttons
    document.querySelectorAll(".settings").forEach((buttons) => {
      buttons.classList.remove("settings-disabled")
    })

    // Resetting stats and text
    document.querySelector("#wpm-score").textContent = "-"
    document.querySelector("#acc-score").textContent = "-"
    // Shown time goes back to selected value
    timeManager.changeTimeShown(false, document.querySelector("div.settings input[name='mode']:checked").value)
    timeManager.cancelTimeout()
    // "Restart Test" button hidden
    document.querySelector("#restart-bar").style.display = "none"
    // Blur comes back on the text area
    document.querySelector("#start-msg").style.display = ""
    document.querySelector("#start-msg-content").style.display = ""

  },

  changeEndPopup(scenario){
    switch (scenario) {
      case "default":
        document.querySelector("#dialog-title h1").textContent = dialogMessages.default.title
        document.querySelector("#dialog-title p").textContent = dialogMessages.default.text
        document.querySelector("#dialog-title img").src = "./assets/images/icon-completed.svg"
        break
      case "baseline":
        document.querySelector("#dialog-title h1").textContent = dialogMessages.baseline.title
        document.querySelector("#dialog-title p").textContent = dialogMessages.baseline.text
        document.querySelector("#dialog-title img").src = "./assets/images/icon-completed.svg"
        break
      case "pb":
        // TODO : confetti animation
        document.querySelector("#dialog-title h1").textContent = dialogMessages.pb.title
        document.querySelector("#dialog-title p").textContent = dialogMessages.pb.text
        document.querySelector("#dialog-title img").src = "./assets/images/icon-new-pb.svg"
        break
      default:
        console.error("Error: unknown game end scenario")
        return
    }
  },
  endGame() {
    console.log("Debug: game ended")
    this.gameState = "off"

    if (storageAvailable("localStorage")) {
      localStorage.setItem("totalPlayed", ++this.totalPlayed)
    }

    // Updating dialog box with stats
    document.querySelector("#wpm-final-score").textContent = this.computeFinalWpm()
    document.querySelector("#acc-final-score").textContent = this.computeAccuracy()
    document.querySelector("#typed-final-score").textContent = this.typed
    document.querySelector("#mistakes-final-score").textContent = this.mistakesPermanent

    // Update PB if needed
    if (this.pb < this.computeFinalWpm()) {
      console.log("Debug: New PB score")

      this.pb = this.computeFinalWpm()
      if (storageAvailable("localStorage")) {
        localStorage.setItem("pbWpm", this.pb)
      }
      document.querySelector("#pb-wpm-score").textContent = this.pb
      this.changeEndPopup("pb")
    } else {
      this.changeEndPopup("default")
    }
    if (this.totalPlayed==1) {
      this.changeEndPopup("baseline")
    }
    document.querySelector("#complete-popup").showModal()

    this.resetGame()
  }
}

